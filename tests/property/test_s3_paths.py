"""
Property 9: Client Data Isolation
===================================

For ANY client_id string, all S3 paths generated by the S3PathManager for that
client SHALL contain the client_id as a path component. Furthermore, for ANY
path and any different client_id, the path validation function SHALL return
False (rejecting cross-client access).

This is the security backbone — if this property holds, data can never leak
between clients.

Validates: Requirements 10.1, 10.3, 10.4

# Feature: churn-prediction-pipeline, Property 9: Client Data Isolation
"""

import pytest
from hypothesis import given, settings, assume
from hypothesis import strategies as st

from churn_pipeline.s3_paths import S3PathManager


# Strategy for valid client IDs (alphanumeric + hyphens/underscores)
_client_id = st.text(
    alphabet=st.characters(whitelist_categories=("L", "N"), whitelist_characters="_-"),
    min_size=3,
    max_size=30,
).filter(lambda s: s[0].isalpha())

# Strategy for bucket names
_bucket = st.text(
    alphabet=st.characters(whitelist_categories=("Ll", "N"), whitelist_characters="-"),
    min_size=3,
    max_size=20,
).filter(lambda s: s[0].isalpha())

# Strategy for date strings
_date = st.from_regex(r"20[2-3][0-9]-[01][0-9]-[0-3][0-9]", fullmatch=True)

# Strategy for version strings
_version = st.text(
    alphabet=st.characters(whitelist_categories=("L", "N"), whitelist_characters="_-"),
    min_size=1,
    max_size=15,
)


@pytest.mark.property
@given(
    bucket=_bucket,
    client_id=_client_id,
    date=_date,
    version=_version,
)
@settings(max_examples=200)
def test_all_paths_contain_client_id(
    bucket: str, client_id: str, date: str, version: str
) -> None:
    """
    Every path generated by S3PathManager must contain the client_id.
    """
    pm = S3PathManager(bucket, client_id)

    # Collect all possible paths
    paths = [
        pm.raw_data_path(date),
        pm.config_path(),
        pm.draft_config_path(),
        pm.processed_path(date),
        pm.model_path(version),
        pm.model_card_path(version),
        pm.feature_artifacts_path(version),
        pm.output_path(date),
        pm.monitoring_path(date),
        pm.training_stats_path(),
    ]

    for path in paths:
        # Client ID must appear as a path component
        assert f"/{client_id}/" in path, (
            f"Path '{path}' does not contain client_id '{client_id}' as a component"
        )
        # Path must start with the expected base
        assert path.startswith(f"s3://{bucket}/{client_id}/"), (
            f"Path '{path}' does not start with expected prefix "
            f"'s3://{bucket}/{client_id}/'"
        )


@pytest.mark.property
@given(
    bucket=_bucket,
    client_id=_client_id,
    other_client_id=_client_id,
    date=_date,
)
@settings(max_examples=200)
def test_validation_rejects_other_clients(
    bucket: str, client_id: str, other_client_id: str, date: str
) -> None:
    """
    Paths from one client must be rejected by another client's validator.
    """
    assume(client_id != other_client_id)

    # Generate a path for client A
    pm_a = S3PathManager(bucket, client_id)
    path_a = pm_a.raw_data_path(date)

    # Validate it with client B's manager — must reject
    pm_b = S3PathManager(bucket, other_client_id)
    assert not pm_b.validate_path_belongs_to_client(path_a), (
        f"Client '{other_client_id}' incorrectly accepted path '{path_a}' "
        f"belonging to client '{client_id}'"
    )

    # Validate it with client A's manager — must accept
    assert pm_a.validate_path_belongs_to_client(path_a), (
        f"Client '{client_id}' incorrectly rejected its own path '{path_a}'"
    )


@pytest.mark.property
@given(
    bucket=_bucket,
    client_id=_client_id,
    date=_date,
    version=_version,
)
@settings(max_examples=200)
def test_validation_accepts_own_paths(
    bucket: str, client_id: str, date: str, version: str
) -> None:
    """
    All paths generated by a client's manager must be accepted by that same manager.
    """
    pm = S3PathManager(bucket, client_id)

    paths = [
        pm.raw_data_path(date),
        pm.config_path(),
        pm.draft_config_path(),
        pm.processed_path(date),
        pm.model_path(version),
        pm.model_card_path(version),
        pm.feature_artifacts_path(version),
        pm.output_path(date),
        pm.monitoring_path(date),
        pm.training_stats_path(),
    ]

    for path in paths:
        assert pm.validate_path_belongs_to_client(path), (
            f"Manager rejected its own path: '{path}'"
        )
